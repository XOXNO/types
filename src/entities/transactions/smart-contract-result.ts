import { ApiProperty, ApiPropertyOptional } from '@nestjs/swagger';
import { TransactionLog } from './transaction-log';

export class SmartContractResult {
  @ApiProperty({
    description: 'The hash of the smart contract result',
    example:
      '0x1234567890abcdef1234567890abcdef1234567890abcdef1234567890abcdef',
    type: String,
  })
  hash = '';

  @ApiProperty({
    description:
      'The Unix timestamp when the smart contract result was created',
    example: 1672531200,
    type: Number,
  })
  timestamp = 0;

  @ApiProperty({
    description: 'The nonce of the smart contract result',
    example: 0,
    type: Number,
  })
  nonce = 0;

  @ApiProperty({
    description: 'The gas limit for the smart contract result',
    example: 50000,
    type: Number,
  })
  gasLimit = 0;

  @ApiProperty({
    description: 'The gas price for the smart contract result',
    example: 1000000000,
    type: Number,
  })
  gasPrice = 0;

  @ApiProperty({
    description: 'The value transferred in the smart contract result',
    example: '1000000000000000000',
    type: String,
  })
  value = '';

  @ApiProperty({
    description: 'The sender address of the smart contract result',
    example: 'erd1qqqqqqqqqqqqqpgqrc4pg2xarca9z34njcxeur622qmfjp8w2jps89fxnl',
    type: String,
  })
  sender = '';

  @ApiProperty({
    description: 'The receiver address of the smart contract result',
    example: 'erd1spyavw0956vq68xj8y4tenjpq2wd5a9p2c6j8gsz7ztyrnpxrruqzu66jx',
    type: String,
  })
  receiver = '';

  @ApiProperty({
    description: 'The relayed value in the smart contract result',
    example: '0',
    type: String,
  })
  relayedValue = '';

  @ApiProperty({
    description: 'The data field of the smart contract result',
    example: '@6f6b',
    type: String,
  })
  data = '';

  @ApiProperty({
    description: 'The hash of the previous transaction in the chain',
    example:
      '0xabcdef1234567890abcdef1234567890abcdef1234567890abcdef1234567890',
    type: String,
  })
  prevTxHash = '';

  @ApiProperty({
    description:
      'The hash of the original transaction that triggered this result',
    example:
      '0x9876543210fedcba9876543210fedcba9876543210fedcba9876543210fedcba',
    type: String,
  })
  originalTxHash = '';

  @ApiProperty({
    description: 'The type of smart contract call',
    example: 'directCall',
    type: String,
  })
  callType = '';

  @ApiPropertyOptional({
    description:
      'The hash of the miniblock containing this smart contract result',
    example:
      '0xfedcba9876543210fedcba9876543210fedcba9876543210fedcba9876543210',
    type: String,
  })
  miniBlockHash: string | undefined = undefined;

  @ApiPropertyOptional({
    description: 'The logs generated by this smart contract result',
    type: TransactionLog,
  })
  logs: TransactionLog | undefined = undefined;

  @ApiPropertyOptional({
    description: 'The return message from the smart contract execution',
    example: 'ok',
    type: String,
  })
  returnMessage: string | undefined = undefined;
}
